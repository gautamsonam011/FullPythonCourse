import math

x = 23.45
y = 10.03
z = -10
a = 5
# ceil()------>

print(math.ceil(x))
print(math.ceil(y))

# fabs()------->

print(math.fabs(x))   # this function does not return neg. value
print(math.fabs(z))

# factorial()------>

print(math.factorial(a))
# print(math.factorial(x))  'float' object cannot be interpreted as an integer

# floor()--------->

print(math.floor(x))
print(math.floor(y))

# fsum()------------> 

l = [34,56,87,21]
print(math.fsum(l))

# sqrt()----------> 

print(math.sqrt(16))
print(math.sqrt(144))
